package com.wakatech.invatarejuridica.networking;

public class ProfileInfo {
    public String nume;
    public String email;
    public String scoala;
    public int rank;
    public String intrebari;

    public ProfileInfo(String nume, String email, String scoala, int rank, String intrebari) {
        this.nume = nume;
        this.email = email;
        this.scoala = scoala;
        this.rank = rank;
        this.intrebari = intrebari;

    }
}
